package com.licenta.yogi.ui.addproducts;

import androidx.lifecycle.MutableLiveData;
import androidx.lifecycle.ViewModel;

public class AddProductsViewModel extends ViewModel {

    private MutableLiveData<String> productName,productSymbol,productTotal,productHeight,productWidth,productLength;

    public AddProductsViewModel() {
        productName  = new MutableLiveData<>();
        productSymbol = new MutableLiveData<>();
        productTotal = new MutableLiveData<>();
        productHeight  = new MutableLiveData<>();
        productLength = new MutableLiveData<>();
        productWidth = new MutableLiveData<>();
    }

    public MutableLiveData<String> getProductName() {
        return productName;
    }

    public void setProductName(MutableLiveData<String> productName) {
        this.productName = productName;
    }

    public MutableLiveData<String> getProductSymbol() {
        return productSymbol;
    }

    public void setProductSymbol(MutableLiveData<String> productSymbol) {
        this.productSymbol = productSymbol;
    }

    public MutableLiveData<String> getProductTotal() {
        return productTotal;
    }

    public void setProductTotal(MutableLiveData<String> productTotal) {
        this.productTotal = productTotal;
    }

    public MutableLiveData<String> getProductHeight() {
        return productHeight;
    }

    public void setProductHeight(MutableLiveData<String> productHeight) {
        this.productHeight = productHeight;
    }

    public MutableLiveData<String> getProductWidth() {
        return productWidth;
    }

    public void setProductWidth(MutableLiveData<String> productWidth) {
        this.productWidth = productWidth;
    }

    public MutableLiveData<String> getProductLength() {
        return productLength;
    }

    public void setProductLength(MutableLiveData<String> productLength) {
        this.productLength = productLength;
    }
}